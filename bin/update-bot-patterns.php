#!/usr/bin/env php
<?php

/**
 * Fetches the latest bot patterns from Matomo's device-detector YAML
 * and regenerates data/bot-patterns.php.
 *
 * Run manually:   php bin/update-bot-patterns.php
 * Via composer:    composer update (runs automatically)
 *
 * Compares the remote file's ETag/hash against the last known hash
 * stored in data/.bot-patterns-hash to skip unnecessary updates.
 */

$yaml_url  = 'https://raw.githubusercontent.com/matomo-org/device-detector/master/regexes/bots.yml';
$output    = __DIR__ . '/../data/bot-patterns.php';
$hash_file = __DIR__ . '/../data/.bot-patterns-hash';

echo "Fetching bot patterns from Matomo device-detector...\n";

// Fetch the YAML content.
$context = stream_context_create( [
	'http' => [
		'timeout'    => 15,
		'user_agent' => 'Mai-Analytics-Bot-Updater/1.0',
	],
] );

$yaml = @file_get_contents( $yaml_url, false, $context );

if ( false === $yaml ) {
	echo "Warning: Could not fetch bot patterns from GitHub. Keeping existing patterns.\n";
	exit( 0 );
}

// Check if content has changed.
$new_hash = md5( $yaml );
$old_hash = file_exists( $hash_file ) ? trim( file_get_contents( $hash_file ) ) : '';

if ( $new_hash === $old_hash ) {
	echo "Bot patterns are already up to date.\n";
	exit( 0 );
}

// Extract bot name patterns from the YAML.
// The YAML structure has entries like:
//   - regex: 'Googlebot(-Image|-Video)?'
//     name: 'Googlebot'
//
// We extract the 'regex' values and simplify them to plain substrings
// by stripping regex syntax. For patterns that are already plain strings,
// they pass through unchanged.
$patterns = [];

if ( preg_match_all( '/^\s*-\s*regex:\s*[\'"](.+?)[\'"]/m', $yaml, $matches ) ) {
	foreach ( $matches[1] as $regex ) {
		// Simplify regex to a plain substring for stripos matching:
		// - Remove optional groups like (-Image|-Video)?
		// - Remove anchors ^ $
		// - Remove common regex quantifiers
		// - Remove escape sequences for literal dots
		$simplified = $regex;
		$simplified = preg_replace( '/\([^)]*\)\??/', '', $simplified );  // Remove optional groups.
		$simplified = preg_replace( '/[\^$]/', '', $simplified );          // Remove anchors.
		$simplified = preg_replace( '/[+*?]/', '', $simplified );          // Remove quantifiers.
		$simplified = str_replace( [ '\\/', '\\.' ], [ '/', '.' ], $simplified ); // Unescape.
		$simplified = preg_replace( '/\\\\./', '', $simplified );          // Remove remaining escapes.
		$simplified = preg_replace( '/\[.*?\]/', '', $simplified );        // Remove character classes.
		$simplified = trim( $simplified );

		// Only keep patterns that are at least 5 chars with 4+ consecutive letters.
		// Short fragments from regex simplification cause false positives.
		if ( strlen( $simplified ) >= 5 && preg_match( '/[a-zA-Z]{4,}/', $simplified ) ) {
			$patterns[] = $simplified;
		}
	}
}

// Also extract bot names as fallback patterns.
if ( preg_match_all( '/^\s*name:\s*[\'"](.+?)[\'"]/m', $yaml, $matches ) ) {
	foreach ( $matches[1] as $name ) {
		$name = trim( $name );

		// Bot names must be at least 5 chars with 4+ letters to avoid generic matches.
		if ( strlen( $name ) >= 5 && preg_match( '/[a-zA-Z]{4,}/', $name ) ) {
			$patterns[] = $name;
		}
	}
}

// Deduplicate and sort.
$patterns = array_unique( $patterns );
sort( $patterns );

// Remove patterns that are substrings of common browser user-agents.
$browser_false_positives = [
	'Chrome', 'Safari', 'Firefox', 'Mozilla', 'Opera', 'Edge', 'Mobile',
	'Android', 'Linux', 'Windows', 'Macintosh', 'Apple', 'Web', 'HTTP',
	'Internet', 'Explorer', '.com', 'Yahoo', 'Mail', 'Outlook', 'Samsung',
];
$patterns = array_filter( $patterns, function( $pattern ) use ( $browser_false_positives ) {
	foreach ( $browser_false_positives as $fp ) {
		if ( strcasecmp( $pattern, $fp ) === 0 ) {
			return false;
		}
	}
	return true;
} );

$patterns = array_values( $patterns );

if ( empty( $patterns ) ) {
	echo "Warning: No patterns extracted. Keeping existing patterns.\n";
	exit( 0 );
}

// Generate the PHP file.
$header = <<<'PHP'
<?php

// Exit if accessed directly.
if ( ! defined( 'ABSPATH' ) ) exit;

/**
 * Bot user-agent patterns extracted from Matomo's device-detector.
 * Auto-generated by bin/update-bot-patterns.php — do not edit manually.
 *
 * @link https://github.com/matomo-org/device-detector/blob/master/regexes/bots.yml
 */
return [
PHP;

$lines = [];
foreach ( $patterns as $pattern ) {
	$escaped = addslashes( $pattern );
	$lines[] = "\t'{$escaped}',";
}

$php = $header . "\n" . implode( "\n", $lines ) . "\n];\n";

file_put_contents( $output, $php );
file_put_contents( $hash_file, $new_hash );

echo sprintf( "Updated %d bot patterns in data/bot-patterns.php\n", count( $patterns ) );
